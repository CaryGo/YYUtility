// Define the buffer size
#define YYCATEGORIESLIB_BUFFER_SIZE 1024

- (NSData *)yy_gzipCompress {
    // Allocate memory for the compressed data
    NSMutableData* compressedData = [NSMutableData dataWithLength:YYCATEGORIESLIB_BUFFER_SIZE];
    // Initialize the zlib stream
    z_stream stream;
    stream.zalloc = Z_NULL;
    stream.zfree = Z_NULL;
    stream.opaque = Z_NULL;
    stream.avail_in = (uInt)self.length;
    stream.next_in = (Bytef*)self.bytes;
    stream.avail_out = (uInt)compressedData.length;
    stream.next_out = (Bytef*)compressedData.mutableBytes;
    // Compress the data
    int result = deflateInit2(&stream, Z_DEFAULT_COMPRESSION, Z_DEFLATED, 31, 8, Z_DEFAULT_STRATEGY);
    if (result == Z_OK) {
        do {
            result = deflate(&stream, Z_FINISH);
        } while (result == Z_OK);
        deflateEnd(&stream);
        if (result == Z_STREAM_END) {
            compressedData.length = stream.total_out;
            return compressedData;
        }
    }
    return nil;
}

- (NSData *)yy_gzipDecompress {
    // Allocate memory for the decompressed data
    NSMutableData* decompressedData = [NSMutableData dataWithLength:YYCATEGORIESLIB_BUFFER_SIZE];
    // Initialize the zlib stream
    z_stream stream;
    stream.zalloc = Z_NULL;
    stream.zfree = Z_NULL;
    stream.opaque = Z_NULL;
    stream.avail_in = (uInt)self.length;
    stream.next_in = (Bytef*)self.bytes;
    stream.avail_out = (uInt)decompressedData.length;
    stream.next_out = (Bytef*)decompressedData.mutableBytes;
    // Decompress the data
    int result = inflateInit2(&stream, 31);
    if (result == Z_OK) {
        do {
            result = inflate(&stream, Z_FINISH);
        } while (result == Z_OK);
        inflateEnd(&stream);
        if (result == Z_STREAM_END) {
            decompressedData.length = stream.total_out;
            return decompressedData;
        }
    }
    return nil;
}

